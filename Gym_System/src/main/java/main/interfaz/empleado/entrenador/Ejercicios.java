    private void bt_CrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_CrearActionPerformed
        // TODO add your handling code here:
        String idStr = txt_ID.getText();
        int id_rutina = Integer.parseInt(idStr);
        String ordenStr = txt_Orden.getText();
        int orden = Integer.parseInt(ordenStr);
        
        Ejercicio selec = (Ejercicio) com_Ejercicio.getSelectedItem();
        if (selec == null) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar un ejercicio");
            return;
        }
        int id_ejercicio = selec.getId_ejercicio();
        
        Rutina_Ejercicio rutEje = new Rutina_Ejercicio(id_rutina, id_ejercicio, orden);
        rutEjeDao.crear(rutEje);
        JOptionPane.showMessageDialog(this, "Ejercicio creado");
        Utils.mostrarPanel(puerta, new lista_Rutinas(dpi_entrenador));
    }//GEN-LAST:event_bt_CrearActionPerformed


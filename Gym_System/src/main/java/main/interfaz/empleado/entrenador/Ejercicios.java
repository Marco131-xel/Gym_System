    private String nombreCliente(int id) {
        Rutina rut = rutDao.buscar(id);
        if (rut != null) {
            String nombre = Utils.nombreCliente(rut.getDpi_cliente());
            return nombre;
        } else {
            return "Desconocido";
        }
    }

    private void cargarEjercicios() {
        com_Ejercicio.removeAllItems();
        List<Ejercicio> lista = ejeDao.listar();

        for (Ejercicio eje : lista) {
            com_Ejercicio.addItem(eje);
        }
    }


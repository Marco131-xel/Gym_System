    private void bt_CambiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_CambiarActionPerformed
        // TODO add your handling code here:
        String fechaInicio = txt_FechaInicio.getText().trim();
        String fechaFin = txt_FechaFin.getText().trim();
        System.out.println("id del cambio: " + id_Asignacion);
        if (fechaInicio.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes ingresar fecha de inicio");
            return;
        }

        if (fechaFin.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes ingresar fecha de fin");
            return;
        }
        java.sql.Date fechaInicioSQL;
        java.sql.Date fechaFinSQL;
        try {
            fechaInicioSQL = java.sql.Date.valueOf(fechaInicio);
            fechaFinSQL = java.sql.Date.valueOf(fechaFin);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this,
                    "Formato de fecha inválido. Usa yyyy-MM-dd");
            return;
        }

        Empleado selec = (Empleado) com_Coach.getSelectedItem();
        if (selec == null) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar un entrenador");
            return;
        }

        long dpi_entrenador = selec.getDpi();

        Entrenador_Cliente enCi = new Entrenador_Cliente(id_Asignacion, dpi_entrenador, dpi_cliente, fechaInicioSQL, fechaFinSQL);
        System.out.println("=== MODIFICACIÓN REALIZADA ===");
        System.out.println("ID Asignación : " + id_Asignacion);
        System.out.println("Entrenador    : " + dpi_entrenador + " (" + selec.getNombre() + ")");
        System.out.println("Cliente       : " + dpi_cliente);
        System.out.println("Fecha Inicio  : " + fechaInicioSQL);
        System.out.println("Fecha Fin     : " + fechaFinSQL);
        System.out.println("==============================");
        enCiDao.actualizar(enCi);
        JOptionPane.showMessageDialog(this, "modificacion Creada");
        Utils.mostrarPanel(puerta, new ListaClientes());
    }//GEN-LAST:event_bt_CambiarActionPerformed

    private void cargarCoach() {
        com_Coach.removeAllItems();
        List<Empleado> lista = dao.listar();
        int sucursal = Utils.datosEmpleado(Recepcionista.usuario);

        for (Empleado emp : lista) {
            if (emp.getIdRol() == 2 && emp.getIdSucursal() == sucursal) {
                com_Coach.addItem(emp);
            }
        }
    }


    public void cargarLista() {
        DefaultTableModel modelo = (DefaultTableModel) tab_Pagos.getModel();
        modelo.setRowCount(0);
        List<Pago> lista = dao.listar();
        
        for (Pago pago : lista) {
            modelo.addRow(new Object[]{
                pago.getId(),
                pago.getDpi(),
                nombreCliente(pago.getDpi()),
                pago.getTipo(),
                pago.getMonto(),
                nombreAdicional(pago.getId_adicional()),
            });
        }
    }
    
    public String nombreAdicional(int id) {
        Adicional adi = adiDao.buscar(id);
        if (adi != null) {
            String nombre = adi.getNombre();
            return nombre;
        } else {
            return "    ------";
        }
    }
    
    public String nombreCliente(long dpi) {
        Cliente cli = cliDao.buscar(dpi);
        if (cli != null) {
            String nombre = cli.getNombre();
            String apellido = cli.getApellido();
            return nombre + " " + apellido;
        } else {
            return "Desconocido";
        }
    }
    

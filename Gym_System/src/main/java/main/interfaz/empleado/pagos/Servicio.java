    private void bt_PagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_PagarActionPerformed
        // TODO add your handling code here:
        String descripcion = txt_Descripcion.getText();
        double monton = Double.parseDouble(txt_Monto.getText().trim());
        String fecha_Inicio = txt_FechaInicio.getText().trim();
        String fecha_Fin = txt_FechaFin.getText().trim();

        if (fecha_Inicio.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes ingresar fecha de inicio");
            return;
        }
        if (fecha_Fin.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes ingresar fecha de fin");
            return;
        }
        java.sql.Date fechaInicioSQL;
        java.sql.Date fechaFinSQL;
        try {
            fechaInicioSQL = java.sql.Date.valueOf(fecha_Inicio);
            fechaFinSQL = java.sql.Date.valueOf(fecha_Fin);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this,
                    "Formato de fecha inv√°lido. Usa yyyy-MM-dd");
            return;
        }

        Pago pago = new Pago(dpi, tipo, descripcion, monton, fechaInicioSQL, fechaFinSQL);
        dao.crear(pago);
    }//GEN-LAST:event_bt_PagarActionPerformed
    
    public String membresia() {
        MembresiasDao meDao = new MembresiasDao();
        Membresia mem = meDao.buscarPorDpi(dpi);
        int tipo = mem.getTipo();
        String stipo = Utils.getIdTipo(tipo);
        
        return stipo;
    }


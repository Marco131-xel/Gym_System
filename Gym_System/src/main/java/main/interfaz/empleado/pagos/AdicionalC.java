    private void com_AdiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_com_AdiActionPerformed
        // TODO add your handling code here:
        Adicional seleccionado = (Adicional) com_Adi.getSelectedItem();
        if (seleccionado != null) {
            txt_Descripcion.setText(seleccionado.getDetalles());
            txt_Monto.setText(String.valueOf(seleccionado.getPrecio()));
            // entrenador
            Empleado emp = emDao.buscarPorDPI(seleccionado.getDpi());

            if (emp != null) {
                String nombre = emp.getNombre();
                String apellido = emp.getApellido();
                txt_Entrenador.setText(nombre + " " + apellido);
            } else {
                txt_Entrenador.setText("");
            }
        }
    }//GEN-LAST:event_com_AdiActionPerformed

    private void bt_PagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_PagarActionPerformed
        // TODO add your handling code here:
        String descripcion = txt_Descripcion.getText();
        double monton = Double.parseDouble(txt_Monto.getText().trim());
        String fecha = txt_Fecha.getText().trim();

        if (fecha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes ingresar fecha");
            return;
        }

        java.sql.Date fechaSQL;
        try {
            fechaSQL = java.sql.Date.valueOf(fecha);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this,
                    "Formato de fecha inv√°lido. Usa yyyy-MM-dd");
            return;
        }

        // validar id de adicional
        Adicional selec = (Adicional) com_Adi.getSelectedItem();
        if (selec == null) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar un adicional");
            return;
        }
        
        int id_Adi = selec.getId();
        
        Pago pago = new Pago(dpi, tipo, descripcion, monton, fechaSQL, id_Adi);
        paDao.crear(pago);
    }//GEN-LAST:event_bt_PagarActionPerformed

    private void cargarAdicionales() {
        com_Adi.removeAllItems();
        List<Adicional> lista = adiDao.listar();

        for (Adicional adi : lista) {
            com_Adi.addItem(adi);
        }

        if (!lista.isEmpty()) {
            com_Adi.setSelectedIndex(0);
            Adicional primero = lista.get(0);

            txt_Descripcion.setText(primero.getDetalles());
            txt_Monto.setText(String.valueOf(primero.getPrecio()));

            if (primero.getDpi() > 0) {
                Empleado emp = emDao.buscarPorDPI(primero.getDpi());
                if (emp != null) {
                    txt_Entrenador.setText(emp.getNombre() + " " + emp.getApellido());
                } else {
                    txt_Entrenador.setText("Sin entrenador");
                }
            } else {
                txt_Entrenador.setText("Sin entrenador");
            }
        }
    }


